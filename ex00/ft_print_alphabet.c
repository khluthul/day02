#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}
void	ft_print_alphabet()
{
	char c;

	c = 'a';
	while(c >= 97 && c <= 122)
	{
		ft_putchar(c++);
	}
}
