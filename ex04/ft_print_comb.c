#include <unistd.h>

void	ft_print_comb()
{
	int d1;
	int d2;
	int d3;

	d1 = 0;
	while( d1 >= 0)
	{
		while(d2 >= 0)
		{
			while(d3 >= 0)
			{
				if(d1 == d3 || d2 == d1)
					d3 = d3 + 1;
				write(1, &d3, 1);
			}
			if(d1 == d2)
				d2 = d2 +1;
			write(1, &d3, 1);
		}
		write(1, &d1, 1);
	}
}
