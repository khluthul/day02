#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}
void	ft_print_numbers()
{
	char c;

	c = 48;
	while(c >= 48 && c <= 57)
	{
		ft_putchar(c++);
	}
}
